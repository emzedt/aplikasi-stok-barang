    private void ButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonLoginActionPerformed
        try{
            user = txtUsername.getText(); //variabel user digunakan untuk menampung value dari text field txtUsername
            pass = txtPassword.getText(); //variabel pass digunakan untuk menampung value dari password field txtPassword
           
            sql = "SELECT * FROM tbsignup WHERE username=? AND password=?"; //variabel sql menampung syntax mengambil data username dan password dari database tbsignup
            PreparedStatement pst = con.prepareStatement(sql); //penggunaan objek PreparedStatement untuk eksekusi query SQL
            pst.setString(1, user); //kolom username
            pst.setString(2, pass); //kolom password
            rs = pst.executeQuery(); //eksekusi pernyataan
            
            if(rs.next()){ //Mengembalikan nilai berikutnya jika cocok dengan pola yang dibuat dari string yang ditentukan.
                JOptionPane.showMessageDialog(null, "Login Berhasil!"); //menampilkan output Login Berhasil!
                clear(); //method clear untuk membersihkan text field
                new Dashboard().setVisible(true); //method yang membuat JFrame TiketBioskop muncul di layar
                this.dispose(); //menutup JFrame Login
            }
            else{
                JOptionPane.showMessageDialog(null, "Login Gagal!"); //menampilkan output Login Gagal!
                clear(); //method clear untuk membersihkan text field
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage()); //jika try tidak berhasil akan ada petunjuk errornya dimana
        }
    }//GEN-LAST:event_ButtonLoginActionPerformed

