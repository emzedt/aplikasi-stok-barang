    private void ButtonCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCetakActionPerformed
        String fromkd = txtfromkd.getText(); //menampung value txtfromkd pada variabel fromkd
        String tokd = txttokd.getText(); //menampung value txttokd pada variabel tokd
        
        HashMap a = new HashMap(); //menyimpan key dan value pair
        a.put("fromkd", fromkd);
        a.put("tokd", tokd);
        
        try{
            JasperDesign jDesign = JRXmlLoader.load("C:\\Users\\MSI Modern 14\\Documents\\NetBeansProjects\\UAS_31210053\\src\\View"
                    + "\\ReportStokBarang.jrxml");
            JasperReport jReport = JasperCompileManager.compileReport(jDesign);
            JasperPrint jPrint = JasperFillManager.fillReport(jReport, a, con);
            JasperViewer.viewReport(jPrint);
        } catch(Exception e){ //Jika try error
            JOptionPane.showMessageDialog(null, e.getMessage()); //menampilkan errornya dimana lewat showMessage
        }
    }//GEN-LAST:event_ButtonCetakActionPerformed

