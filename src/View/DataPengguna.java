    private void showTable(){ //method menampilkan data-data dari database tbbioskop ke tabel
        DefaultTableModel model = (DefaultTableModel) tabelPengguna.getModel(); //DefaultTableModel digunakan untuk memberikan header dan data pada kolom dan baris tabel 
        try
        {
            sql= "SELECT * FROM tbsignup GROUP BY 1"; //mengambil data dari tbsignup dan mengembalikan semua nilai yang dikelompokkan berdasarkan username
            rs = stm.executeQuery(sql);/* Method ini digunakan untuk mengeksekusi pernyataan yang mengembalikan data yang terstruktur ke dalam baris, 
                                yang masing-masing berisi informasi tentang sesuatu. Ini mengembalikan objek dari kelas ResultSet. */

            while(rs.next()) //Mengembalikan nilai berikutnya jika cocok dengan pola yang dibuat dari string yang ditentukan.
            {
                model.addRow(new Object[]{rs.getString("username"), rs.getString("password"), rs.getString("nama")
                        , rs.getString("tempat"), rs.getString("tanggallahir"), rs.getString("jeniskelamin"), rs.getString("alamat")
                        , rs.getString("agama"), rs.getString("status")});
                //menambahkan data ke JTable secara urut mulai dari username, password, nama, tempat, tanggallahir, jeniskelamin, alamat, agama, status
            }
        } catch (Exception e) //akan aktif jika try gagal
          {
            JOptionPane.showMessageDialog(this, e.getMessage()); //Jika ada error di method showTable, maka akan menampilkan popup errornya dimana
          }
    }
