    private void ButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSaveActionPerformed
        txtKodeBarang.getText(); //value txtKodeBarang
        txtNamaBarang.getText(); //value txtNamaBarang
        kdsatuan = BoxKodeSatuan.getSelectedItem().toString(); //mengubah item yang dipilih pada BoxKodeSatuan menjadi string ditampung oleh variabel kdsatuan
        satuan = BoxSatuan.getSelectedItem().toString(); //mengubah item yang dipilih pada BoxSatuan menjadi string, ditampung oleh variabel satuan
        txtHargaBeli.getText(); //value txtHaragaBeli
        quantity = SpinQuantity.getValue().toString(); //mengubah value spinQuantity menjadi string, ditampung oleh variabel quantity

        try {  
                String query = "insert into tbdatabarang" + "(kode, namabarang, kd_satuan, satuan, hargabeli, quantity)" 
                        + "values (?,?,?,?,?,?)";
                //syntax memasukkan value ke tbdatabarang 
                java.sql.PreparedStatement pst = con.prepareStatement(query); //penggunaan objek PreparedStatement untuk eksekusi query SQL

                pst.setString(1, txtKodeBarang.getText()); //kolom kode
                pst.setString(2, txtNamaBarang.getText()); //kolom namabarang
                pst.setString(3, kdsatuan); //kolom kd_satuan
                pst.setString(4, satuan); //kolom satuan
                pst.setString(5, txtHargaBeli.getText()); //kolom hargabeli
                pst.setString(6, quantity); //kolom quantity
                pst.executeUpdate(); //eksekusi pernyataan
                {
                    JOptionPane.showMessageDialog(null, "Save Succesfull"); //akan muncul pop up Save Successfull setelah proses insert selesai
                }
                clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage()); //jika try tidak berhasil akan menampilkan pop up ERRORnya dimana
        }
    }//GEN-LAST:event_ButtonSaveActionPerformed

