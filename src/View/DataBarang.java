    public void updateComboBoxKodeSatuan(){ //mengubah value kd_satuan tbsatuanbarang menjadi bagian dari item BoxKodeSatuan
        String query = "SELECT * FROM tbsatuanbarang"; //mengambil value dari tbsatuanbarang
        try {
            java.sql.PreparedStatement pst = con.prepareStatement(query); //penggunaan objek PreparedStatement untuk eksekusi query SQL
            rs = pst.executeQuery(query); //eksekusi query, hasilnya ada di rs
            
            while (rs.next()) {  //Mengembalikan nilai berikutnya jika cocok dengan pola yang dibuat dari string yang ditentukan.              
                BoxKodeSatuan.addItem(rs.getString("kd_satuan")); //menambahkan semua value kd_satuan ke item BoxKodeSatuan
            }
        } catch (Exception e) { //jika try gagal
            JOptionPane.showMessageDialog(this, e.getMessage()); //menampilkan error dimana lewat showMessage
        }
    }
    
