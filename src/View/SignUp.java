    private void ButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSaveActionPerformed
        pass=txtPassword.getText(); //variabel pass digunakan untuk menampung value dari password field txtPassword
        repass=txtRepassword.getText(); //variabel repass digunakan untuk menampung value dari password field txtRepassword
        panjangUser=txtUsername.getText().length(); //variabel panjangUser digunakan untuk menampung value dari text field txtUsername
        
        try {
            if (repass.equals(pass) && panjangUser>=2) { /*Jika variabel pass dan repass memiliki value yang sama dan panjangUser katanya lebih dari 2
                                                           maka akan memasukkan value yang sudah diisi di txtUsername, txtPassword, dan txtRepassword ke dalam database tbsignup*/
                String query = "insert into tbsignup" + "(username, password, repassword, nama, tempat, tanggallahir, jeniskelamin"
                        + ", alamat, agama, status)" +
                        "values (?,?,?,?,?,?,?,?,?,?)";
                        //syntax memasukkan value ke tbsignup 
                java.sql.PreparedStatement pst = con.prepareStatement(query); //penggunaan objek PreparedStatement untuk eksekusi query SQL
                
                pst.setString(1, txtUsername.getText()); //kolom username
                pst.setString(2, String.valueOf(txtPassword.getPassword())); //kolom password
                pst.setString(3, String.valueOf(txtRepassword.getPassword())); //kolom repassword
                pst.setString(4, txtNama.getText()); //kolom nama
                pst.setString(5, txtTempat.getText()); //kolom tempat
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd"); //adalah kelas konkret untuk memformat dan mengurai tanggal dengan cara yang peka terhadap lokal. Ini memungkinkan untuk memformat (tanggal -> teks), parsing (teks -> tanggal), dan normalisasi.
                String tanggal = sdf.format(DateTglLahir.getDate()); //memformat DateTglLahir dan akan ditampung oleh variabel tanggal
                pst.setString(6, tanggal); //kolom tanggallahir
                jk = BoxJenisKelamin.getSelectedItem().toString(); //mengambil value combo box BoxJenisKelamin dan mengubahnya ke bentuk string. Kemudian ditampung valuenya oleh variabel jk
                pst.setString(7, jk); //kolom jeniskelamin
                pst.setString(8, txtAlamat.getText()); //kolom alamat
                agama = BoxAgama.getSelectedItem().toString(); //mengambil value combo box BoxAgama dan mengubahnya ke bentuk string. Kemudian ditampung valuenya oleh variabel agama
                pst.setString(9, agama); //kolom agama
                status = BoxStatus.getSelectedItem().toString(); //mengambil value combo box BoxStatus dan mengubahnya ke bentuk string. Kemudian ditampung valuenya oleh variabel status
                pst.setString(10, status); //kolom status
                pst.executeUpdate(); //eksekusi pernyataan
                {
                    JOptionPane.showMessageDialog(null, "Save Succesfull"); //akan muncul pop up Save Successfull setelah proses insert selesai
                }
                clear(); //method menghapus value text field yang diisi user
            } else { //jika pass dan repass tidak sama atau panjangUser < 2 akan menampilkan output Access Denied
                JOptionPane.showMessageDialog(null, "Access Denied");
                clear(); //method menghapus value text field yang diisi user
            };
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage()); //jika try tidak berhasil akan menampilkan pop up ERROR
        }
    }//GEN-LAST:event_ButtonSaveActionPerformed

